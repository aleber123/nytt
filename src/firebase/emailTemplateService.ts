const SETTINGS_DOC_ID = '__design_settings__';

export async function getEmailDesignSettings(): Promise<EmailDesignSettings> {
  try {
    const snap = await getDoc(doc(db, COLLECTION, SETTINGS_DOC_ID));
    if (snap.exists()) {
      return { ...DEFAULT_DESIGN_SETTINGS, ...snap.data() } as EmailDesignSettings;
    }
  } catch {
    // Fall back to defaults
  }
  return DEFAULT_DESIGN_SETTINGS;
}

export async function saveEmailDesignSettings(settings: EmailDesignSettings): Promise<void> {
  await setDoc(doc(db, COLLECTION, SETTINGS_DOC_ID), {
    ...settings,
    updatedAt: serverTimestamp(),
  });
}

