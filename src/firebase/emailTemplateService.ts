// SHARED EMAIL WRAPPER — generates consistent HTML for all emails
// ============================================================

export function generateEmailWrapper(options: {
  settings: EmailDesignSettings;
  lang: 'sv' | 'en';
  title: string;
  headerTitle: string;
  headerSubtitle?: string;
  bodyContent: string;
  showTrackingButton?: boolean;
  orderNumber?: string;
  showContactSection?: boolean;
}): string {
  const { settings: s, lang, title, headerTitle, headerSubtitle, bodyContent } = options;
  const isEn = lang === 'en';
  const showContact = options.showContactSection !== false;
  const showTracking = options.showTrackingButton && options.orderNumber;

  // Logo: clean, no border/padding/background. Wrapped in a link to the company site.
  // Height fixed via inline style; width auto-scales. Inserted as block to allow centering.
  const logoImg = s.showLogo ? `<img src="${s.logoUrl}" alt="${s.companyName}" height="${s.logoHeight}" style="height:${s.logoHeight}px;width:auto;border:0;outline:none;text-decoration:none;display:block;">` : '';
  const logoHtml = s.showLogo
    ? `<a href="${s.logoLinkUrl}" target="_blank" style="display:inline-block;text-decoration:none;border:0;outline:none;">${logoImg}</a>`
    : '';

  const trackingHtml = showTracking ? `
    <div style="background:#f0fdf4; border:2px solid ${s.successColor}; border-radius:8px; padding:20px; margin:22px 0; text-align:center;">
      <h3 style="color:#166534; margin:0 0 10px; font-size:17px;">${isEn ? 'Track Your Order' : 'Följ din order'}</h3>
      <p style="color:#15803d; margin:0 0 14px; font-size:14px;">${isEn ? 'Follow the progress of your application in real-time:' : 'Följ ditt ärende i realtid:'}</p>
      <a href="https://doxvl.se/orderstatus?order=${options.orderNumber}" style="display:inline-block; background:${s.successColor}; color:#fff; padding:12px 28px; border-radius:6px; text-decoration:none; font-weight:700; font-size:15px;">${isEn ? 'Track Order Status' : 'Se orderstatus'}</a>
    </div>
  ` : '';

  const contactHtml = showContact ? `
    <div style="background:#f0f9ff; border:1px solid #bae6fd; border-radius:8px; padding:18px; margin:22px 0; text-align:center;">
      <h3 style="margin:0 0 8px;">${isEn ? 'Questions?' : 'Har du frågor?'}</h3>
      <p style="margin:0;">${isEn ? 'Feel free to contact us:' : 'Kontakta oss gärna:'}</p>
      <p style="margin:8px 0 0;">📧 <a href="mailto:${s.contactEmail}" style="color:${s.primaryColor}">${s.contactEmail}</a> &nbsp; 📞 ${s.contactPhone}</p>
    </div>
  ` : '';

  return `<!DOCTYPE html>
<html lang="${lang}">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${title}</title>
</head>
<body style="font-family:${s.fontFamily}; line-height:1.6; color:#202124; max-width:${s.bodyMaxWidth}px; margin:0 auto; background-color:#f8f9fa; padding:20px;">
  <div style="background-color:#ffffff; border-radius:${s.borderRadius}px; box-shadow:0 2px 6px rgba(0,0,0,0.08); overflow:hidden;">
    <div style="background:${s.headerBackground}; color:${s.headerTextColor}; padding:32px 36px 24px; text-align:${s.logoAlign}; border-bottom:2px solid ${s.headerBorderColor};">
      ${logoHtml}
      ${s.showHeaderTitle ? `<h1 style="margin:${s.showLogo ? '20px' : '0'} 0 0; font-size:22px; font-weight:700; letter-spacing:0.2px; color:${s.headerTextColor};">${headerTitle}</h1>` : ''}
      ${headerSubtitle && s.showHeaderTitle ? `<p style="color:${s.headerTextColor}; opacity:0.8; margin:8px 0 0;">${headerSubtitle}</p>` : ''}
    </div>
    <div style="padding:32px 36px;">
      ${bodyContent}
      ${trackingHtml}
      ${contactHtml}
    </div>
    <div style="background:${s.footerBackground}; padding:24px 36px; text-align:center; border-top:1px solid #eaecef;">
      <p style="margin:5px 0; color:${s.footerTextColor}; font-size:13px;"><strong>${s.companyName}</strong></p>
      <p style="margin:5px 0; color:${s.footerTextColor}; font-size:13px;">${isEn ? s.companyTaglineEn : s.companyTagline}</p>
      <p style="margin:5px 0; color:${s.footerTextColor}; font-size:13px;">${isEn ? s.autoGeneratedNoteEn : s.autoGeneratedNote}</p>
    </div>
  </div>
</body>
</html>`.trim();
}

