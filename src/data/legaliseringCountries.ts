/**
 * Legalization Country Data
 *
 * Comprehensive data for programmatic generation of /legalisering/[country] pages.
 * Countries that already have dedicated static pages (qatar.tsx, etc.) are excluded
 * since Next.js gives static files priority over dynamic routes.
 */

export interface LegaliseringCountry {
  slug: string;
  countryCode: string; // ISO 3166-1 alpha-2
  name: string; // Swedish
  nameEn: string;
  /** Whether this country is a Hague Apostille Convention member */
  apostille: boolean;
  /** Primary gradient color for hero section */
  heroColor: string;
  heroColorDark: string;
  /** Embassy/consulate in Stockholm or nearest */
  embassy: {
    name: string;
    nameEn: string;
    city: string;
    address?: string;
    phone?: string;
    email?: string;
    website?: string;
  };
  /** Country-specific reasons for legalization */
  reasons: string[];
  reasonsEn: string[];
  /** Common documents */
  documents: string[];
  documentsEn: string[];
  /** Processing time estimate */
  processingDays: string;
  /** Price range in SEK */
  priceLow: string;
  priceHigh: string;
  /** FAQ items */
  faq: { q: string; a: string; qEn: string; aEn: string }[];
  /** Popular flag — shown on index page */
  popular?: boolean;
  /** Region for grouping */
  region: 'middle-east' | 'africa' | 'asia' | 'europe' | 'americas' | 'oceania';
}

