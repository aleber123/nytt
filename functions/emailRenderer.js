const ESCAPE_HTML_MAP = { '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' };
function escapeHtml(str) {
  return String(str).replace(/[&<>"']/g, ch => ESCAPE_HTML_MAP[ch]);
}

function substituteVariables(template, variables, variableSchema = []) {
  const isHtmlMap = new Map(variableSchema.map(v => [v.key, !!v.isHtml]));
  return template.replace(/\{\{(\w+)\}\}/g, (_m, key) => {
    const value = variables[key];
    if (value == null || value === '') return '';
    const str = String(value);
    return isHtmlMap.get(key) ? str : escapeHtml(str);
  });
}

function formatBodyAsHtml(body, htmlVariableKeys = new Set()) {
  if (!body) return '';
  if (/<\s*(p|div|table|h[1-6]|ul|ol|blockquote|hr|br)[\s>]/i.test(body)) return body;
  const paragraphs = body
    .replace(/\r\n/g, '\n')
    .split(/\n{2,}/)
    .map(p => p.trim())
    .filter(p => p.length > 0);
  return paragraphs.map(p => {
    const onlyVarMatch = p.match(/^\{\{(\w+)\}\}$/);
    if (onlyVarMatch && htmlVariableKeys.has(onlyVarMatch[1])) return p;
    return `<p>${p.replace(/\n/g, '<br>')}</p>`;
  }).join('\n');
}

