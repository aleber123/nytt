async function getTemplate(id) {
  try {
    const snap = await admin.firestore().collection(COLLECTION).doc(id).get();
    if (!snap.exists) return null;
    return { id: snap.id, ...snap.data() };
  } catch (err) {
    console.warn(`[emailRenderer] Failed to load template "${id}"`, err);
    return null;
  }
}

async function getDesignSettings() {
  try {
    const snap = await admin.firestore().collection(COLLECTION).doc(SETTINGS_DOC_ID).get();
    if (!snap.exists) return DEFAULT_DESIGN_SETTINGS;
    return { ...DEFAULT_DESIGN_SETTINGS, ...snap.data() };
  } catch {
    return DEFAULT_DESIGN_SETTINGS;
  }
}

/**
 * Render an email from the catalog. Returns { subject, html, rendered }.
 * When `rendered` is false the caller should fall back to hardcoded defaults.
 */
async function renderEmail(templateId, variables, locale = 'sv', options = {}) {
  const template = await getTemplate(templateId);
  if (!template) return { subject: '', html: '', rendered: false };

  const settings = await getDesignSettings();
  const subjectTpl = locale === 'en' ? template.subjectEn : template.subjectSv;
  const bodyTpl = locale === 'en' ? template.bodyEn : template.bodySv;

  const subject = substituteVariables(subjectTpl || '', variables, template.variables);
  const htmlVarKeys = new Set((template.variables || []).filter(v => v.isHtml).map(v => v.key));
  const bodyFormatted = formatBodyAsHtml(bodyTpl || '', htmlVarKeys);
  const bodyContent = substituteVariables(bodyFormatted, variables, template.variables);

  const html = generateEmailWrapper({
    settings,
    lang: locale,
    title: subject,
    headerTitle: subject,
    bodyContent,
    showContactSection: options.showContactSection,
  });

  return { subject, html, rendered: true };
}

module.exports = { renderEmail };
